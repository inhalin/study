# 두 수 a와 b를 선언한 뒤, a와 b를 이용한 사칙연산 계산기를 만들어 보세요.

print("<< Simple Calculator 1.0 >>")

print("1. 기본 사칙연산")
a=int(input("첫번째 정수 a를 입력하세요 > "))
b=int(input("두번째 정수 b를 입력하세요 > "))

print("a + b =", a+b)
print("a - b =", a-b)
print("a * b =", a*b)
print("a / b =", a/b)
print("a // b =", a//b)
print("a % b =", a%b)

